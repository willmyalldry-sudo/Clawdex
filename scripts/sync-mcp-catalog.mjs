import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const SOURCE_URL = "https://raw.githubusercontent.com/ever-works/awesome-mcp-servers/master/README.md";
const OUTPUT_PATH = resolve("apps/worker/src/data/mcp-catalog.generated.ts");

const response = await fetch(SOURCE_URL, { headers: { Accept: "text/plain", "User-Agent": "Benjamin-Persyn-Agent-OS" } });
if (!response.ok) throw new Error(`Catalog download failed (${response.status}).`);

const markdown = await response.text();
const entries = parseCatalog(markdown);
if (entries.length < 100) throw new Error(`Catalog parser found only ${entries.length} entries; refusing to replace the snapshot.`);

const generated = `/* This file is generated by scripts/sync-mcp-catalog.mjs. Do not edit manually. */
import type { McpCatalogEntry } from "@agent-os/shared";

export const MCP_CATALOG_SOURCE = ${JSON.stringify(SOURCE_URL)};
export const MCP_CATALOG_SYNCED_AT = ${JSON.stringify(new Date().toISOString())};
export const MCP_CATALOG: McpCatalogEntry[] = ${JSON.stringify(entries, null, 2)};
`;

await mkdir(dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, generated, "utf8");
console.log(JSON.stringify({ output: OUTPUT_PATH, entries: entries.length, categories: new Set(entries.map((entry) => entry.category)).size }));

export function parseCatalog(input) {
  const headings = [...input.matchAll(/^##\s+([^\n#]+)$/gm)].map((match) => ({ index: match.index ?? 0, name: clean(match[1]) }));
  const matches = [...input.matchAll(/^-\s+\[([^\]]+)\]\((https?:\/\/[^)\s]+)\)\s+-\s+([\s\S]*?)(?=^-\s+\[|^##\s+|(?![\s\S]))/gm)];
  const ids = new Map();

  return matches.map((match) => {
    const name = clean(match[1]);
    const sourceUrl = match[2].trim();
    const block = match[3];
    const category = [...headings].reverse().find((heading) => heading.index < (match.index ?? 0))?.name ?? "Uncategorized";
    const tags = [...block.matchAll(/`([^`]+)`/g)].map((tag) => clean(tag[1])).filter(Boolean);
    const description = clean(block
      .replace(/\(\[Read more\]\([^)]+\)\)/g, "")
      .replace(/`[^`]+`/g, "")
      .replace(/\s+/g, " "));
    const baseId = slugify(name) || "mcp-server";
    const duplicate = ids.get(baseId) ?? 0;
    ids.set(baseId, duplicate + 1);
    const id = duplicate === 0 ? baseId : `${baseId}-${duplicate + 1}`;
    const riskLevel = classifyRisk(`${category} ${name} ${description} ${tags.join(" ")}`);
    return { id, name, sourceUrl, category, description, tags, riskLevel };
  });
}

function classifyRisk(input) {
  const text = input.toLowerCase();
  if (["trade", "trading", "swap", "wallet", "token transfer", "mint", "payment", "messaging", "execute code"].some((term) => text.includes(term))) return "critical";
  if (["blockchain", "crypto", "finance", "database", "cloud", "devops", "server management", "file management", "commerce", "automation"].some((term) => text.includes(term))) return "high";
  if (["api integration", "project management", "document", "monitoring", "browser", "search", "data access", "testing", "workflow"].some((term) => text.includes(term))) return "medium";
  return "low";
}

function clean(value = "") {
  return value.replace(/\s+/g, " ").trim();
}

function slugify(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "").slice(0, 80);
}
